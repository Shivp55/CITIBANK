package Model;

public class PinModel {

	int id,pid,pin;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getPid() {
		return pid;
	}

	public void setPid(int pid) {
		this.pid = pid;
	}

	public int getPin() {
		return pin;
	}

	public void setPin(int pin) {
		this.pin = pin;
	}

	@Override
	public String toString() {
		return "PinModel [id=" + id + ", pid=" + pid + ", pin=" + pin + "]";
	}
	
}
